function sc = weather_scenario()
%WEATHER_SCENARIO  极热无风气象场景参数（与 verify/case_data.py 一致）

% ---- 调度时段（单时段快照基准状态）----
sc.dt_h       = 1.0;      % 调度时段长度 (h)，用于爬坡与水电电量约束

% ---- 环境温度 / 风况 / 辐照 ----
sc.T_amb      = 40.0;     % 极热环境温度 (°C)
sc.wind_speed = 2.0;      % 近地面风速 (m/s)，< 切入风速 => 风电近乎为 0
sc.irradiance = 900.0;    % 水平面辐照度 (W/m^2)

% ---- 火电高温降容（一次线性降容模型）----
sc.T_ref_thermal = 35.0;  % 火电额定参考温度 (°C)
sc.alpha_coal    = 0.005; % 燃煤机组温度降容系数 (1/°C)
sc.alpha_gas     = 0.009; % 燃气机组温度降容系数 (1/°C)

% ---- 水电（极热常伴枯水/低水头）----
sc.k_hydro = 0.85;        % 极热枯水可用出力系数（占额定）

% ---- 风电出力曲线 ----
sc.v_cut_in  = 3.0;       % 切入风速 (m/s)
sc.v_rated   = 12.0;      % 额定风速 (m/s)
sc.v_cut_out = 25.0;      % 切出风速 (m/s)

% ---- 光伏（含温度负系数）----
sc.G_stc    = 1000.0;     % 标准测试辐照度 (W/m^2)
sc.T_stc    = 25.0;       % 标准测试温度 (°C)
sc.NOCT     = 45.0;       % 标称工作温度 (°C)
sc.gamma_pv = -0.004;     % 功率温度系数 (1/°C)

% ---- 负荷温度敏感模型 ----
sc.T_L0      = 30.0;      % 负荷舒适基准温度 (°C)
sc.beta_load = 0.020;     % 空调负荷温度灵敏度 (1/°C)
sc.rho_cool  = 0.40;      % 温敏(空调)负荷占节点基荷比例

% ---- 供电可靠性等级（切负荷优先级）----
sc.level_frac = [0.20, 0.30, 0.50];        % 一级/二级/三级 比例
sc.voll       = [10000.0, 5000.0, 1000.0]; % 一级/二级/三级 切负荷惩罚 ($/MWh)
sc.level_name = {'一级(关键)', '二级(重要)', '三级(可中断)'};

end
